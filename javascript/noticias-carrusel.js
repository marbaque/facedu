$ = jQuery;
var self = this;

/* ================================================================================== */
/* == READY ========================================================================= */
/* ================================================================================== */

$(document).ready(init);

function init() {
    var lista = $('.lista-tipos-noticias');
    $.ajax({
        url: Drupal.url('rest/tipos-noticias')
    }).done(function(tipos) {
        lista.append('<li><a href="' + Drupal.url('') + '">Todas</a></li>');
        for (var i = 0; i < tipos.length; i++) {
            lista.append('<li><a href="?categoria=' + tipos[i].tid + ' ">' + tipos[i].name + '</a></li>');
        }
    });
}